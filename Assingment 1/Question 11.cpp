#include <iostream>


using namespace std;
int main()
{
    //Increment operator(++) increases the value of a variable by 1 whilst Decrement operator(--) decreases the value of a variable by 1
    //Example of increment operator
    int w = 50;
    int x =++w;
    cout << "The value of increment is: " << x << endl;


    //Example of Decrement operator
    int y = 72;
    int z = --y;
    cout << "The value of decrement is: " << z << endl;

    return 0;
}
